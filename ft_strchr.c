char	*ft_strchr(const char *s, int c)
{
	int		cont;

	cont = 0;
	while (s[cont] != '\0' && s[cont] != c)
		cont++;
	if (s[cont] == c)
		return ((char *)&s[cont]);
	return (0);
}
